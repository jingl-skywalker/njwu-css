    private void sectorPaint(Graphics g){
       
        g.setColor(Color.BLUE);
        int sum = teaStatic.getOld()+teaStatic.getYoung()+teaStatic.getYoungOld();
        int young=(int) (360*((double)(teaStatic.getYoung())/sum));
        g.fillArc(0, 0, 150, 150, 0, young);
        
        g.setColor(Color.CYAN);
         int youngOld=(int) (360*((double)(teaStatic.getYoungOld())/sum));
        g.fillArc(0, 0, 150, 150, young, youngOld);
        
         g.setColor(Color.GREEN);
         int old = 360 - young-youngOld;
        g.fillArc(0, 0, 150, 150, youngOld+young, old);
    }
    
    private void youngPaint(Graphics g){
        int pheight = youngPanel.getHeight();
        int dhegiht = drawHeight(1);
        int width = youngPanel.getWidth();
        
       Graphics2D l = (Graphics2D) g;
        
        l.setColor(Color.BLUE);
       // youngPanel.setForeground(Color.GREEN);
        l.fill3DRect(0, pheight-dhegiht, width, dhegiht, true);
        //g.fill3DRect(0, 10, width, 20, true);
        
        g.setColor(Color.red);
        String num = teaStatic.getYoung()+"";
        g.drawString(num, 0, pheight-dhegiht+10);
    }
    private void youngOldPaint(Graphics g){
         int pheight = youngOldPanel.getHeight();
        int dhegiht = drawHeight(2);
        int width = oldPanel.getWidth();
         
       g.setColor(Color.cyan);
        g.fill3DRect(0, pheight-dhegiht, width, dhegiht, true);
        
       g.setColor(Color.red);
        String num = teaStatic.getYoungOld()+"";
        g.drawString(num, 0, pheight-dhegiht+10);
    }
    
     private void oldPaint(Graphics g){
         int pheight = oldPanel.getHeight();
        int dhegiht = drawHeight(3);
        int width = oldPanel.getWidth();
        
        g.setColor(Color.GREEN);
        g.fill3DRect(0, pheight-dhegiht, width, dhegiht, true);
        
        g.setColor(Color.red);
        String num = teaStatic.getOld()+"";
        g.drawString(num, 0, pheight-dhegiht+10);
    }
     
     private int drawHeight(int x){
         int height = youngPanel.getHeight()-10;
         int sum = teaStatic.getOld()+teaStatic.getYoung()+teaStatic.getYoungOld();
         if(sum ==0){
            
             return 0;
         }
         if(x==1){
             return (int) (height*((double)(teaStatic.getYoung())/sum));
         }
         else if(x==2){
             return (int) (height*((double)(teaStatic.getYoungOld())/sum));
         }
         else {
              return (int) (height*((double)(teaStatic.getOld())/sum));
         }
     }
     
