    private void showCourseList(ArrayList<CourseVO> list){
        Iterator<CourseVO> iterator  = list.iterator();
        DefaultTableModel tablemodel = (DefaultTableModel)courseListTable.getModel();
        while(iterator.hasNext()){
            CourseVO course = iterator.next();
            String courseName = course.getCourseName();
            String credit = "" + course.getCredit();
            String teacher = course.getTeaName();
            String time = course.getTime();
            String type = course.getProperty();
            
            tablemodel.addRow(new Object[]{courseName, credit, teacher, time, type});
            
        }
    }
    
    public void clearTable(){
        courseListTable.removeAll();
    }
    
    private void termListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_termListMouseClicked
        String term_ch = (String)termList.getSelectedValue();
        String term = term_ch.substring(3);
        if(term_ch.charAt(5) == '一'){
            term += "-1";
        }
        else{
            term += "-2";
        }
        showingCourselist = new ArrayList<CourseVO>();
        Iterator<CourseVO> iterator = courselist.iterator();
        while(iterator.hasNext()){
            CourseVO course = iterator.next();
            String[] termlist = course.getTerm().split(",");
            for(int i = 0; i < term.length(); i++){
                if(termlist[i].equals(term)){
                    showingCourselist.add(course);
                }
            }
        }
        clearTable();
        showCourseList(showingCourselist);
        
    }//GEN-LAST:event_termListMouseClicked

